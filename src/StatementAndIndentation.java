public class StatementAndIndentation {
    public static void main(String[] args) {

        //read about whitespace here, https://google.github.io/styleguide/javaguide.html#s4.6-whitespace

        int myScore = 50; //it called a java statement;

        myScore++; //it is a statement
        myScore--; //it is a statement

        System.out.println("this is a test"); //it is a statement
        System.out.println("final score : " + myScore);
    }
}
